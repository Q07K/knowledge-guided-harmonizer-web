<script>
import { ref, nextTick, onMounted, watch } from 'vue'
import { useHarmonizerApi } from '../composables/useHarmonizerApi.js'
import { validateSqlCreateTable } from './utils.js'
import { marked } from 'marked'
import DOMPurify from 'dompurify'

export default {
  name: 'ChatPanel',
  props: {
    sqlQuery: {
      type: String,
      default: ''
    },
    sqlValidation: {
      type: Object,
      default: null
    }
  },
  emits: ['start-analysis', 'visualization-data'],
  setup(props, { emit }) {
    const currentMessage = ref('')
    const messages = ref([])
    const isLoading = ref(false)
    const messagesContainer = ref(null)
    const messageInput = ref(null)
    const currentNodeState = ref('')
    const currentStreamingMessage = ref('')
    
    const { streamChat, streamInitMessage, sendChatMessage } = useHarmonizerApi()
    
    let messageIdCounter = 0

    // marked ÏÑ§Ï†ï
    marked.setOptions({
      breaks: true,
      gfm: true
    })

    // ÎßàÌÅ¨Îã§Ïö¥ Î†åÎçîÎßÅ Ìï®Ïàò
    const renderMarkdown = (text) => {
      if (!text) return ''
      
      // ÎßàÌÅ¨Îã§Ïö¥ÏùÑ HTMLÎ°ú Î≥ÄÌôò
      const html = marked(text)
      
      // XSS Î∞©ÏßÄÎ•º ÏúÑÌï¥ HTMLÏùÑ Ï†ïÌôî
      return DOMPurify.sanitize(html)
    }

    const addMessage = (text, type = 'user', messageClass = null) => {
      const message = {
        id: ++messageIdCounter,
        text,
        type,
        timestamp: new Date(),
        messageClass
      }
      messages.value.push(message)
      scrollToBottom()
    }

    // Ï≤¥ÌÅ¨Î¶¨Ïä§Ìä∏ Î©îÏãúÏßÄ Ìè¨Îß∑ÌåÖ Ìï®Ïàò
    const formatCheckListMessage = (checkListData) => {
      let formattedMessage = `üìã **${checkListData.topic}**\n\n`
      formattedMessage += 'Îã§Ïùå ÏßàÎ¨∏Îì§Ïóê ÎåÄÌïú ÎãµÎ≥ÄÏùÑ ÌÜµÌï¥ Ïò®ÌÜ®Î°úÏßÄ Î™®Îç∏ÏùÑ Í∞úÏÑ†Ìï† Ïàò ÏûàÏäµÎãàÎã§:\n\n'
      
      checkListData.expansion_questions.forEach((item, index) => {
        const priorityEmoji = item.priority === 'ÎÜíÏùå' ? 'üî¥' : item.priority === 'Î≥¥ÌÜµ' ? 'üü°' : 'üü¢'
        formattedMessage += `${index + 1}. ${priorityEmoji} **[${item.question_type}]** (Ïö∞ÏÑ†ÏàúÏúÑ: ${item.priority})\n`
        formattedMessage += `   ${item.expected_question}\n\n`
      })
      
      if (checkListData.next_steps && checkListData.next_steps.length > 0) {
        formattedMessage += '**Îã§Ïùå Îã®Í≥Ñ:**\n'
        checkListData.next_steps.forEach((step, index) => {
          formattedMessage += `${index + 1}. ${step}\n`
        })
      }
      
      return formattedMessage
    }

    const scrollToBottom = async () => {
      await nextTick()
      if (messagesContainer.value) {
        messagesContainer.value.scrollTop = messagesContainer.value.scrollHeight
      }
    }

    const formatTime = (timestamp) => {
      return timestamp.toLocaleTimeString('ko-KR', { 
        hour: '2-digit', 
        minute: '2-digit' 
      })
    }

    const sendMessage = async () => {
      const message = currentMessage.value.trim()
      if (!message || isLoading.value) return

      // ÏÇ¨Ïö©Ïûê Î©îÏãúÏßÄ Ï∂îÍ∞Ä
      addMessage(message, 'user')
      currentMessage.value = ''

      // AI ÏùëÎãµ Ï≤òÎ¶¨
      isLoading.value = true
      currentNodeState.value = 'API ÏöîÏ≤≠ Ï§ë...'
      currentStreamingMessage.value = ''
      
      try {
        console.log('=== Ïã§Ï†ú Ï±ÑÌåÖ API Ìò∏Ï∂ú ===')
        console.log('Message:', message)
        
        // Ïã§Ï†ú Ï±ÑÌåÖ API Ìò∏Ï∂ú
        const response = await sendChatMessage(message)
        console.log('Chat API Response:', response)
        
        // API ÏùëÎãµ Ï≤òÎ¶¨
        await processApiResponse(response)
        
      } catch (error) {
        console.error('Chat API error:', error)
        currentNodeState.value = 'API Ïò§Î•ò'
        
        let errorMessage = 'Ï±ÑÌåÖ API Ìò∏Ï∂úÏóê Ïã§Ìå®ÌñàÏäµÎãàÎã§.'
        if (error.message) {
          errorMessage += `\nÏò§Î•ò: ${error.message}`
        }
        
        addMessage(errorMessage, 'ai')
        
      } finally {
        isLoading.value = false
        currentNodeState.value = ''
        currentStreamingMessage.value = ''
      }
    }







    const handleKeydown = (event) => {
      if (event.key === 'Enter' && !event.shiftKey) {
        event.preventDefault()
        sendMessage()
      }
    }

    // Ïä§Ìä∏Î¶¨Î∞ç Î©îÏãúÏßÄ Î≥ÄÍ≤ΩÏãú Ïä§ÌÅ¨Î°§ ÏóÖÎç∞Ïù¥Ìä∏
    watch(currentStreamingMessage, () => {
      scrollToBottom()
    })

    // node state Î≥ÄÍ≤ΩÏãú Ïä§ÌÅ¨Î°§ ÏóÖÎç∞Ïù¥Ìä∏
    watch(currentNodeState, () => {
      scrollToBottom()
    })

    // SQL ÏøºÎ¶¨ ÏÉÅÌÉúÏóê Îî∞Î•∏ "Î∂ÑÏÑù ÏãúÏûë" Î≤ÑÌäº Ï∂îÍ∞Ä
    const addAnalysisButton = () => {
      const analysisMessage = {
        id: ++messageIdCounter,
        text: '',
        type: 'ai',
        timestamp: new Date(),
        isAnalysisButton: true
      }
      messages.value.push(analysisMessage)
      scrollToBottom()
    }

    // Î∂ÑÏÑù ÏãúÏûë Ï≤òÎ¶¨ - Ïä§Ìä∏Î¶¨Î∞ç Î∞©ÏãùÏúºÎ°ú /init-message API Ìò∏Ï∂ú
    const startAnalysis = async () => {
      if (!props.sqlQuery || !props.sqlQuery.trim()) {
        addMessage('SQL ÏøºÎ¶¨Í∞Ä ÏûÖÎ†•ÎêòÏßÄ ÏïäÏïòÏäµÎãàÎã§. Î®ºÏ†Ä SQL ÏøºÎ¶¨Î•º ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî.', 'ai')
        return
      }

      // SQL Í≤ÄÏ¶ù ÌôïÏù∏ (Ïù¥ÎØ∏ Ïã§ÏãúÍ∞ÑÏúºÎ°ú Í≤ÄÏ¶ùÎêú Í≤∞Í≥º ÏÇ¨Ïö©)
      if (!props.sqlValidation || !props.sqlValidation.isValid) {
        const validationResult = validateSqlCreateTable(props.sqlQuery)
        if (!validationResult.isValid) {
          addMessage(`SQL Í≤ÄÏ¶ù Ïã§Ìå®: ${validationResult.error}`, 'ai')
          return
        }
      }

      console.log('=== SQL Í≤ÄÏ¶ù ÌÜµÍ≥º, Ïä§Ìä∏Î¶¨Î∞ç API Ìò∏Ï∂ú ÏãúÏûë ===')
      console.log('SQL Query:', props.sqlQuery)
      
      // Î∂ÑÏÑù Ï§ë ÏÉÅÌÉúÎ°ú Î≥ÄÍ≤Ω
      isLoading.value = true
      currentNodeState.value = 'API Ïó∞Í≤∞ Ï§ë...'
      currentStreamingMessage.value = ''
      
      try {
        // Ïä§Ìä∏Î¶¨Î∞ç Î∞©ÏãùÏúºÎ°ú /init-message API Ìò∏Ï∂ú
        console.log('Calling streaming /init-message API...')
        await streamInitMessage(
          props.sqlQuery,
          (data) => {
            console.log('Streaming data received:', data)
            handleStreamingData(data)
          },
          (error) => {
            console.error('Streaming error:', error)
            currentNodeState.value = 'API Ïò§Î•ò'
            addMessage(`Ïä§Ìä∏Î¶¨Î∞ç Ïò§Î•ò: ${error.message}`, 'ai')
          }
        )
        
      } catch (apiError) {
        console.error('API Ìò∏Ï∂ú Ïã§Ìå®:', apiError)
        currentNodeState.value = 'API Ïò§Î•ò'
        
        let errorMessage = 'API Ìò∏Ï∂úÏóê Ïã§Ìå®ÌñàÏäµÎãàÎã§.'
        if (apiError.message) {
          errorMessage += `\nÏò§Î•ò: ${apiError.message}`
        }
        if (apiError.status) {
          errorMessage += `\nÏÉÅÌÉú ÏΩîÎìú: ${apiError.status}`
        }
        
        addMessage(errorMessage, 'ai')
        
      } finally {
        isLoading.value = false
        currentNodeState.value = ''
        currentStreamingMessage.value = ''
      }
    }

    // Ïä§Ìä∏Î¶¨Î∞ç Îç∞Ïù¥ÌÑ∞ Ï≤òÎ¶¨ Ìï®Ïàò
    const handleStreamingData = (data) => {
      if (data.state_code === 200 && data.data) {
        const { name, state, message } = data.data
        
        // ÌòÑÏû¨ ÎÖ∏Îìú ÏÉÅÌÉú ÏóÖÎç∞Ïù¥Ìä∏
        if (name) {
          currentNodeState.value = name
        }
        
        // ÏÉÅÌÉúÎ≥Ñ Ï≤òÎ¶¨
        switch (state) {
          case 'current_ontology':
            // Ïò®ÌÜ®Î°úÏßÄ JSON ÌååÏã± ÌõÑ ÏãúÍ∞ÅÌôî Ìå®ÎÑêÎ°ú Ï†ÑÏÜ°
            try {
              const ontologyData = typeof message === 'string' ? JSON.parse(message) : message
              console.log('Ontology data parsed:', ontologyData)
              emit('visualization-data', ontologyData)
              
              // Ï±ÑÌåÖÏóêÎèÑ ÏöîÏïΩ Î©îÏãúÏßÄ Ï∂îÍ∞Ä
              addMessage(`Ïò®ÌÜ®Î°úÏßÄ Ïä§ÌÇ§ÎßàÍ∞Ä ÏÉùÏÑ±ÎêòÏóàÏäµÎãàÎã§.\n\n**ÎèÑÎ©îÏù∏:** ${ontologyData.domain}\n${ontologyData.relation_types.length}Í∞ú`, 'ai', 'success')
            } catch (parseError) {
              console.error('Ontology parsing error:', parseError)
              addMessage('Ïò®ÌÜ®Î°úÏßÄ Îç∞Ïù¥ÌÑ∞ ÌååÏã± Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.', 'ai', 'error')
            }
            break
            
          case 'check_list_items':
            // Ï≤¥ÌÅ¨Î¶¨Ïä§Ìä∏ Î©îÏãúÏßÄ Ìè¨Îß∑ÌåÖ ÌõÑ Ï±ÑÌåÖÏóê Ï∂îÍ∞Ä
            try {
              const checkListData = typeof message === 'string' ? JSON.parse(message) : message
              console.log('Checklist data parsed:', checkListData)
              const formattedMessage = formatCheckListMessage(checkListData)
              addMessage(formattedMessage, 'ai', 'checklist')
            } catch (parseError) {
              console.error('Checklist parsing error:', parseError)
              addMessage('Ï≤¥ÌÅ¨Î¶¨Ïä§Ìä∏ Îç∞Ïù¥ÌÑ∞ ÌååÏã± Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.', 'ai', 'error')
            }
            break
            
          case 'sql_query_analyzed':
            // SQL Î∂ÑÏÑù ÏôÑÎ£å Î©îÏãúÏßÄ
            addMessage(message, 'ai', 'success')
            break
            
          default:
            // Í∏∞ÌÉÄ Î©îÏãúÏßÄÎì§
            if (message) {
              addMessage(message, 'ai')
            }
            break
        }
      }
    }

    // API ÏùëÎãµ Ï≤òÎ¶¨ Ìï®Ïàò
    const processApiResponse = async (response) => {
      console.log('Processing API response:', response)
      
      // node_state ÌëúÏãú
      if (response.node_state) {
        currentNodeState.value = response.node_state
      } else if (response.status) {
        currentNodeState.value = response.status
      } else {
        currentNodeState.value = 'Î∂ÑÏÑù ÏôÑÎ£å'
      }
      
      // ÏùëÎãµ Î©îÏãúÏßÄ Íµ¨ÏÑ±
      let responseMessage = ''
      
      if (response.message) {
        responseMessage = response.message
      } else if (response.result) {
        responseMessage = response.result
      } else if (response.analysis) {
        responseMessage = response.analysis
      } else if (response.data) {
        responseMessage = typeof response.data === 'string' 
          ? response.data 
          : JSON.stringify(response.data, null, 2)
      } else {
        responseMessage = 'Î∂ÑÏÑùÏù¥ ÏôÑÎ£åÎêòÏóàÏäµÎãàÎã§.\n\n' + JSON.stringify(response, null, 2)
      }
      
      // Î©îÏãúÏßÄÎ•º ÌÉÄÏù¥Ìïë Ìö®Í≥ºÎ°ú ÌëúÏãú
      if (responseMessage) {
        await typeMessage(responseMessage)
        addMessage(responseMessage, 'ai')
      }
      
      // ÏãúÍ∞ÅÌôî Îç∞Ïù¥ÌÑ∞ Ï≤òÎ¶¨
      if (response.visualization_data) {
        console.log('Visualization data found:', response.visualization_data)
        emit('visualization-data', response.visualization_data)
      } else if (response.graph_data) {
        console.log('Graph data found:', response.graph_data)
        emit('visualization-data', response.graph_data)
      } else if (response.nodes && response.edges) {
        console.log('Nodes and edges found')
        emit('visualization-data', { nodes: response.nodes, edges: response.edges })
      } else if (response.ontology) {
        console.log('Ontology data found:', response.ontology)
        emit('visualization-data', response.ontology)
      }
    }

    // ÌÉÄÏù¥Ìïë Ìö®Í≥º Ìï®Ïàò
    const typeMessage = async (message) => {
      currentStreamingMessage.value = ''
      const words = message.split(' ')
      
      for (let i = 0; i < words.length; i++) {
        currentStreamingMessage.value = words.slice(0, i + 1).join(' ')
        await nextTick()
        await new Promise(resolve => setTimeout(resolve, 30)) // Îπ†Î•∏ ÌÉÄÏù¥Ìïë
      }
    }

    // SQL ÏøºÎ¶¨ÏôÄ Í≤ÄÏ¶ù Í≤∞Í≥º Î≥ÄÍ≤Ω Í∞êÏßÄ
    watch([() => props.sqlQuery, () => props.sqlValidation], ([newQuery, validation]) => {
      // Í∏∞Ï°¥ Î©îÏãúÏßÄ Ï¥àÍ∏∞Ìôî
      messages.value = []
      messageIdCounter = 0
      
      if (newQuery && newQuery.trim()) {
        if (validation === null) {
          // Í≤ÄÏ¶ù Ï§ëÏù∏ Í≤ΩÏö∞
          addMessage('SQL ÏøºÎ¶¨ Í≤ÄÏ¶ù Ï§ëÏûÖÎãàÎã§...', 'ai', 'validating')
        } else if (validation && validation.isValid) {
          // Í≤ÄÏ¶ù ÏÑ±Í≥µÌïú Í≤ΩÏö∞
          addMessage('SQL ÏøºÎ¶¨Í∞Ä Ïú†Ìö®Ìï©ÎãàÎã§. Î∂ÑÏÑùÏùÑ ÏãúÏûëÌïòÏãúÍ≤†ÏäµÎãàÍπå?', 'ai', 'success')
          addAnalysisButton()
        } else if (validation && !validation.isValid) {
          // Í≤ÄÏ¶ù Ïã§Ìå®Ìïú Í≤ΩÏö∞
          addMessage('Ïò¨Î∞îÎ•∏ CREATE TABLE Íµ¨Î¨∏ÏùÑ ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî.', 'ai', 'error')
        } else {
          // Í≤ÄÏ¶ù Í≤∞Í≥ºÍ∞Ä ÏóÜÎäî Í≤ΩÏö∞ (Ï¥àÍ∏∞ ÏÉÅÌÉú)
          addMessage('SQL ÏøºÎ¶¨Í∞Ä ÏûÖÎ†•ÎêòÏóàÏäµÎãàÎã§. Í≤ÄÏ¶ù Ï§ë...', 'ai', 'validating')
        }
      } else {
        // SQL ÏøºÎ¶¨Í∞Ä ÏóÜÏúºÎ©¥ ÏûÖÎ†• ÏöîÏ≤≠ Î©îÏãúÏßÄ
        addMessage('ÏïàÎÖïÌïòÏÑ∏Ïöî! SQL ÏøºÎ¶¨Î•º Î®ºÏ†Ä ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî. CREATE TABLE Íµ¨Î¨∏ÏùÑ ÏûÖÎ†•ÌïòÏãúÎ©¥ Ïò®ÌÜ®Î°úÏßÄ Í∏∞Î∞ò Î∂ÑÏÑùÏùÑ ÏãúÏûëÌï† Ïàò ÏûàÏäµÎãàÎã§.', 'ai')
      }
    }, { immediate: true })

    onMounted(() => {

    })

    return {
      currentMessage,
      messages,
      isLoading,
      messagesContainer,
      messageInput,
      currentNodeState,
      currentStreamingMessage,
      sendMessage,
      handleKeydown,
      formatTime,
      startAnalysis,
      renderMarkdown
    }
  }
}
</script>

