                <span v-if="!isLoading">Î∂ÑÏÑù ÏãúÏûë</span>
                <span v-else>Î∂ÑÏÑù Ï§ë...</span>
              </button>
            </div>
            <div class="message-time">{{ formatTime(message.timestamp) }}</div>
          </div>
        </div>
        
        <div v-if="isLoading" class="message ai">
          <div class="message-content">
            <!-- Ïä§Ìä∏Î¶¨Î∞ç Ï§ëÏù∏ Î©îÏãúÏßÄÍ∞Ä ÏûàÏúºÎ©¥ ÌëúÏãú -->
            <div v-if="currentStreamingMessage" class="message-text streaming">
              <span v-html="renderMarkdown(currentStreamingMessage)"></span>
              <span class="cursor-blink">|</span>
            </div>
            <!-- Ïä§Ìä∏Î¶¨Î∞ç Î©îÏãúÏßÄÍ∞Ä ÏóÜÏúºÎ©¥ ÌÉÄÏù¥Ìïë Ïù∏ÎîîÏºÄÏù¥ÌÑ∞ ÌëúÏãú -->
