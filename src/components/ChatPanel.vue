            <!-- node state ÌëúÏãú -->
            <div v-if="currentNodeState" class="node-state-indicator">
              <div class="node-state-badge">
                <span class="node-state-text">{{ currentNodeState }}</span>
              </div>
            </div>

          </div>
        </div>

      </div>

      <div class="input-container">
        <div class="input-wrapper">
          <textarea 
            v-model="currentMessage"
            @keydown="handleKeydown"
            placeholder="Î©îÏãúÏßÄÎ•º ÏûÖÎ†•ÌïòÏÑ∏Ïöî.&#10;(Shift+EnterÎ°ú ÏÉà Ï§Ñ, EnterÎ°ú Ï†ÑÏÜ°)"
            class="message-input"
            ref="messageInput"
          ></textarea>
          <button 
            @click="sendMessage"
            :disabled="!currentMessage.trim() || isLoading"
            class="send-button"
          >
            <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2">
              <line x1="22" y1="2" x2="11" y2="13"></line>
